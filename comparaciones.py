#La sintaxis básica de un condicional en Python se estructura utilizando 
# las palabras clave if, elif y else, seguidas de la condición a evaluar 
# y un bloque de código que se ejecutará si la condición es verdadera

# Declaración de variables
numero_uno = 5
numero_dos = 7

# Condicional if
if numero_uno > numero_dos:
    print("El número uno es mayor que el número dos")
# Condicional elif
elif numero_dos > numero_uno:
    print("El número dos es mayor que el número uno")
# Condicional else
else:
    print("Los números son iguales")
